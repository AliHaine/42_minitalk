#include "../../minitalk.h"

int	ft_putstr(char *str)
{
	int		i;
	size_t	v;

	i = 0;
	while (str[i])
	{
		v = write(1, &str[i], 1);
		if (((int )v) == -1)
			return (-1);
		i++;
	}
	return (0);
}
